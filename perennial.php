           <p>A perennial plant or simply perennial is a plant that lives for more than two years.[1] The term (per- + -ennial, "through the years") is often used to differentiate a plant from shorter-lived annuals and biennials. The term is also widely used to distinguish plants with little or no woody growth from trees and shrubs, which are also technically perennials.</p>

<p>Perennials, especially small flowering plants, that grow and bloom over the spring and summer, die back every autumn and winter, and then return in the spring from their rootstock, are known as herbaceous perennials. However, depending on the rigors of local climate, a plant that is a perennial in its native habitat, or in a milder garden, may be treated by a gardener as an annual and planted out every year, from seed, from cuttings or from divisions. Tomato vines, for example, live several years in their natural tropical/subtropical habitat but are grown as annuals in temperate regions because they don't survive the winter.</p>

<p>There is also a class of evergreen, or non-herbaceous, perennials, including plants like Bergenia which retain a mantle of leaves throughout the year. An intermediate class of plants is known as subshrubs, which retain a vestigial woody structure in winter, e.g. Penstemon. The local climate may dictate whether plants are treated as shrubs or perennials. For instance, many varieties of Fuchsia are shrubs in warm regions, but in colder temperate climates may be cut to the ground every year as a result of winter frosts.</p>
